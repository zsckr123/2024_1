
pierwsza_liczba = int(input('Podaj pierwsza liczbe='))
druga_liczba = int(input('Podaj drugą liczbe='))
reszta = pierwsza_liczba % druga_liczba
dziel_calk = pierwsza_liczba // druga_liczba
print(f'reszta z dzielenia = {reszta}')
print(f' dzielnie całkowite={dziel_calk}')

